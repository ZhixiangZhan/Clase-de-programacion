#33. Programa un código que permita contar el número de vocales de la siguiente frase: No hay mal que dure cien años

var1=input("Introduzca una frase: ")
a=var1.count("a")
e=var1.count("e")
i=var1.count("i")
o=var1.count("o")
u=var1.count("u")
print(f"En esta frase hay a:{a},e:{e},i:{i},o:{o},u:{u}")
